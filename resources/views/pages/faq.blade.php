        <x-h type="h1">FAQs</x-h>
        <h3 class="font-semibold">What is conveyancing?</h3>
        <x-p>Conveyancing is the legal process of transferring property ownership from one person to another. It
            involves various steps, including preparing and reviewing contracts, conducting property searches,
            and managing the settlement process. Using a licensed conveyancer ensures all legal aspects are
            handled professionally, minimizing risks and ensuring compliance with relevant laws and
            regulations.
        </x-p>

        <h3 class="font-semibold">Why should I use a licensed conveyancer?</h3>
        <x-p>A licensed conveyancer ensures that all legal aspects of your property transaction are handled
            professionally, minimizing risks and ensuring compliance with relevant laws and regulations. They
            provide the necessary expertise to navigate complex property transactions and protect your
            interests.
        </x-p>


        <h3 class="font-semibold">How long does the conveyancing process take?</h3>
        <x-p>Typically, the conveyancing process takes 4-12 weeks, but it can vary based on the complexity of the
            transaction and other factors.
        </x-p>


        <h3 class="font-semibold">What is the difference between a conveyancer and a
            solicitor?
        </h3>
        <x-p>Both conveyancers and solicitors can handle property transactions, but solicitors are qualified to
            offer a broader range of legal services. Conveyancers specialize in property law and may be more
            cost-effective for straightforward transactions.
        </x-p>


        <h3 class="font-semibold">What are the costs involved in conveyancing?</h3>
        <x-p>Conveyancing costs typically include professional fees for the conveyancer, search fees, and
            government fees for property registration and stamp duty. Always ask for a detailed quote upfront to
            understand all potential costs.
        </x-p>


        <h3 class="font-semibold">What is a cooling-off period?</h3>
        <x-p>A cooling off period is a timeframe during which the buyer can withdraw from the contract of sale
            without significant penalties. In NSW, the standard cooling-off period for residential property is
            five business days.
        </x-p>


        <h3 class="font-semibold">What happens on settlement day?</h3>
        <x-p>On settlement day, the buyer’s conveyancer will transfer the purchase money to the seller, and the
            seller’s conveyancer will hand over the title documents. The property’s ownership officially changes
            hands, and the buyer can take possession of the property.
        </x-p>


        <h3 class="font-semibold">Do I need a conveyancer if I'm buying off the plan?</h3>
        <x-p>Yes, having a conveyancer for off the plan purchases is crucial. They will review the contract,
            ensure all terms are fair, and help you understand your rights and obligations before and after the
            property's construction.
        </x-p>


        <h3 class="font-semibold">What is PEXA and how does it work?</h3>
        <x-p><a class="underline" href="https://www.pexa.com.au/" target="_blank">PEXA (Property Exchange
                Australia)</a> is an online property exchange network that allows for digital property
            settlements. It helps streamline the conveyancing process, reducing paperwork and settlement delays.
        </x-p>

