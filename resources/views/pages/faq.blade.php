                    <h3 class="font-semibold">What is a cooling-off period?</h3>
                    <p>A cooling off period is a timeframe during which the buyer can withdraw from the contract of sale without significant penalties. In NSW, the standard cooling-off period for residential property is five business days.</p>
                </div>
                <div class="text-gray-500 space-y-4">
                    <h3 class="font-semibold">What happens on settlement day?</h3>
                    <p>On settlement day, the buyer’s conveyancer will transfer the purchase money to the seller, and the seller’s conveyancer will hand over the title documents. The property’s ownership officially changes hands, and the buyer can take possession of the property.</p>
                </div>
