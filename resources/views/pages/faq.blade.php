                    <h3 class="font-semibold">How long does the conveyancing process take?</h3>
                    <p>Typically, the conveyancing process takes 4-12 weeks, but it can vary based on the complexity of the transaction and other factors.</p>
                </div>
                <div class="text-gray-500 space-y-4">
                    <h3 class="font-semibold">What is the difference between a conveyancer and a solicitor?</h3>
                    <p>Both conveyancers and solicitors can handle property transactions, but solicitors are qualified to offer a broader range of legal services. Conveyancers specialize in property law and may be more cost-effective for straightforward transactions.</p>
                </div>
                <div class="text-gray-500 space-y-4">
                    <h3 class="font-semibold">What are the costs involved in conveyancing?</h3>
                    <p>Conveyancing costs typically include professional fees for the conveyancer, search fees, and government fees for property registration and stamp duty. Always ask for a detailed quote upfront to understand all potential costs.</p>
